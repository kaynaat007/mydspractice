def check(prefix_a, prefix_b, a, b,  n, m,  length):
    """
    checks if a subarray of length exits in B in O(n) time
    """

    print('checking for length: {}'.format(length))

    store_b = defaultdict(list)

    for left in range(1, m - length + 2): # collect all subarrays of length and store their indices
        right = left + length - 1
        curr_sum = prefix_b[right] - prefix_b[left - 1]
        store_b[curr_sum].append((left-1, right-1))

    # print('store b: {}'.format(store_b))

    for left in range(1, n - length + 2):  # check if any subarray of that size in A is also in B.
        right = left + length - 1
        # print('left: {}, right: {}'.format(left, right))
        curr_sum = prefix_a[right] - prefix_a[left-1]
        if curr_sum in store_b:
            for u, v in store_b[curr_sum]:
                if check_arrays_equal(a[left-1:right], b[u:v+1]):
                    return True

    return False


def check_arrays_equal(a, b):

     print('checking arrays: {}, {}'.format(a, b))
     n = len(a)
     m = len(b)

     if n != m:
         return False
     for i in range(n):
         if a[i] != b[i]:
             return False
     return True


def get_prefix(a):

    n  = len(a)
    prefix_array = [0 for _ in range(n+1)]
    prefix_array[1] = a[0]
    for i in range(2, n+1):
        prefix_array[i] = prefix_array[i-1] + a[i-1]
    return prefix_array


def findLength(A: List[int], B: List[int]):

    n = len(A)
    m = len(B)
    prefix_a = get_prefix(A)
    prefix_b = get_prefix(B)

    low = 0
    high = n
    best = 0
    while low <= high:
        mid = low + (high - low) // 2
        if check(prefix_a, prefix_b, A, B, n, m, mid):
            best = mid
            low = mid + 1
        else:
            high = mid - 1
    return best


